function pickRandom<T>(items: T[]): T {
  return items[Math.floor(Math.random() * items.length)]!;
}

export function generateAnimalNickname(): string {
  const adjective = pickRandom(ADJECTIVES);
  const letter = adjective[0]!.toUpperCase();
  const matchingAnimals = ANIMALS.filter(
    (animal) => animal[0]!.toUpperCase() === letter,
  );
  const animal = pickRandom(
    matchingAnimals.length > 0 ? matchingAnimals : ANIMALS,
  );
  return `${adjective}${animal}`;
}
